    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        String cloName = request.getParameter("cloName");
        poDAO dao = new poDAO();
        PO p = dao.getAllPOBycloName(cloName);
        request.setAttribute("p", p);
        request.getRequestDispatcher("editPO.jsp").forward(request, response);
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        String cloName = request.getParameter("cloName");
        String cloDetails = request.getParameter("cloDetails");
        String loDetails = request.getParameter("loDetails");
        String sid = request.getParameter("syllabusID");
        poDAO dao = new poDAO();     
//        dao.getAllPOBycloName(cloDetails);
        dao.updatePO(cloName, cloDetails, loDetails, sid);
        response.sendRedirect("viewPO?sid=" + sid);
    }

